// Fun stands for function, main name of the function
fun main() {
    println("Hello, world!")
    println("My name is Adem.")
    println("What's your name?")

    // Print function doesn't add a line break like println does.
    print("Adem")
    print(" is my name\n")
    
    // Also we can use \n for a line break with print function.file
    print("My name is\n")
    print("Adem")
    
}